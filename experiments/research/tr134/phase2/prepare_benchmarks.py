# -- MMLU (reuse TR125 pattern) -----------------------------------------------


def _download_mmlu(n_per_subject: int) -> list[dict[str, Any]]:
    from datasets import get_dataset_config_names, load_dataset

    configs = get_dataset_config_names("cais/mmlu")
    subjects = sorted(c for c in configs if c not in MMLU_SKIP_CONFIGS)
    logger.info("MMLU: %d subjects found", len(subjects))

    rng = random.Random(SEED)
    samples: list[dict[str, Any]] = []
    skipped = 0

    for subject in subjects:
        try:
            ds = load_dataset(
                "cais/mmlu", subject, split="test", trust_remote_code=True
            )
        except Exception as e:
            logger.warning("Failed to load MMLU subject %s: %s", subject, e)
            skipped += 1
            continue

        n = min(n_per_subject, len(ds))
        indices = rng.sample(range(len(ds)), n)
        subject_pretty = subject.replace("_", " ").title()

        for idx in sorted(indices):
            row = ds[idx]
            choices = row["choices"]
            correct = int(row["answer"])

            samples.append(
                {
                    "id": f"mmlu_{subject}_{len([s for s in samples if s.get('_subject') == subject]):03d}",
                    "subject": subject_pretty,
                    "question": row["question"],
                    "option_a": choices[0],
                    "option_b": choices[1],
                    "option_c": choices[2],
                    "option_d": choices[3],
                    "reference": MMLU_LABELS[correct],
                    "_subject": subject,
                }
            )

    logger.info("MMLU: %d samples from %d subjects", len(samples), len(subjects) - skipped)
    return samples


