# -- ARC-Challenge (reuse TR125 pattern) --------------------------------------


def _download_arc_challenge(n_samples: int) -> list[dict[str, Any]]:
    from datasets import load_dataset

    ds = load_dataset(
        "allenai/ai2_arc", "ARC-Challenge", split="test", trust_remote_code=True
    )
    logger.info("ARC-Challenge: %d questions in test set", len(ds))

    rng = random.Random(SEED)
    n = min(n_samples, len(ds))
    indices = rng.sample(range(len(ds)), n)

    samples: list[dict[str, Any]] = []
    skipped = 0

    for idx in sorted(indices):
        row = ds[idx]
        labels = row["choices"]["label"]
        texts = row["choices"]["text"]
        answer_key = row["answerKey"]

        label_map = {}
        for i, label in enumerate(labels):
            if label in MMLU_LABELS:
                label_map[label] = label
            elif label.isdigit():
                mapped = MMLU_LABELS[int(label) - 1] if int(label) <= 4 else None
                if mapped:
                    label_map[label] = mapped
            else:
                label_map[label] = label

        ref = label_map.get(answer_key, answer_key)
        if ref not in MMLU_LABELS:
            skipped += 1
            continue

        choice_lines = []
        for label, text in zip(labels, texts, strict=False):
            norm_label = label_map.get(label, label)
            choice_lines.append(f"{norm_label}) {text}")
        choices_text = "\n".join(choice_lines)

        samples.append(
            {
                "id": f"arc_challenge_{len(samples):03d}",
                "question": row["question"],
                "choices_text": choices_text,
                "reference": ref,
            }
        )

    logger.info("ARC-Challenge: %d samples", len(samples))
    return samples


