"""TR125 Phase 2: Download real benchmark data and generate YAML task files.

Downloads MMLU (cais/mmlu) and ARC-Challenge (allenai/ai2_arc) from HuggingFace,
performs stratified/random sampling with a fixed seed, and outputs YAML task files
compatible with the eval framework's generation pipeline.

Output:
  - research/tr125/phase2/tasks/mmlu_real.yaml      (285 questions, 5/subject x 57)
  - research/tr125/phase2/tasks/arc_challenge.yaml   (200 questions)

Usage:
    python research/tr125/phase2/prepare_benchmarks.py [-v] [--force]
    python research/tr125/phase2/prepare_benchmarks.py --mmlu-per-subject 10  # 570 questions
"""

from __future__ import annotations

import argparse
from datetime import UTC, datetime
import hashlib
import logging
from pathlib import Path
import random
import sys
from typing import Any

_DIR = Path(__file__).resolve().parent
_TASKS_DIR = _DIR / "tasks"

logger = logging.getLogger("tr125.phase2.prepare_benchmarks")

SEED = 42
MMLU_PER_SUBJECT = 5
ARC_N_SAMPLES = 200

# Meta configs to skip (not real subjects)
MMLU_SKIP_CONFIGS = {"all", "auxiliary_train"}

MMLU_LABELS = ["A", "B", "C", "D"]


# -- MMLU -------------------------------------------------------------------


def _download_mmlu(n_per_subject: int) -> list[dict[str, Any]]:
    """Download MMLU from HuggingFace and sample n_per_subject from each subject."""
    from datasets import get_dataset_config_names, load_dataset

    configs = get_dataset_config_names("cais/mmlu")
    subjects = sorted(c for c in configs if c not in MMLU_SKIP_CONFIGS)
    logger.info("MMLU: %d subjects found", len(subjects))

    rng = random.Random(SEED)
    samples: list[dict[str, Any]] = []
    skipped = 0

    for subject in subjects:
        try:
            ds = load_dataset(
                "cais/mmlu", subject, split="test", trust_remote_code=True
            )
        except Exception as e:
            logger.warning("Failed to load MMLU subject %s: %s", subject, e)
            skipped += 1
            continue

        n = min(n_per_subject, len(ds))
        indices = rng.sample(range(len(ds)), n)
        subject_pretty = subject.replace("_", " ").title()

        for idx in sorted(indices):
            row = ds[idx]
            choices = row["choices"]
            correct = int(row["answer"])

            samples.append(
                {
                    "id": f"mmlu_{subject}_{len([s for s in samples if s.get('_subject') == subject]):03d}",
                    "subject": subject_pretty,
                    "question": row["question"],
                    "option_a": choices[0],
                    "option_b": choices[1],
                    "option_c": choices[2],
                    "option_d": choices[3],
                    "reference": MMLU_LABELS[correct],
                    "_subject": subject,  # internal, stripped before output
                }
            )

    if skipped:
        logger.warning("Skipped %d MMLU subjects due to download errors", skipped)

    logger.info(
        "MMLU: %d samples from %d subjects", len(samples), len(subjects) - skipped
    )
    return samples


def _write_mmlu_yaml(samples: list[dict[str, Any]], output_path: Path) -> None:
    """Write MMLU samples as a YAML task file."""
    n_subjects = len({s["_subject"] for s in samples})

    lines = [
        f"# Auto-generated by prepare_benchmarks.py on {datetime.now(UTC).strftime('%Y-%m-%d')}",
        f"# Source: cais/mmlu (HuggingFace), test split, seed={SEED}",
        f"# Subjects: {n_subjects}, samples per subject: {len(samples) // max(n_subjects, 1)}",
        f"# Total: {len(samples)} questions",
        "",
        "name: mmlu_real",
        "description: >",
        "  Real MMLU benchmark questions from cais/mmlu (HuggingFace).",
        f"  {n_subjects} subjects, {len(samples)} total questions.",
        "  Scored via exact_match on generated answer letter (A/B/C/D).",
        "task_type: classification",
        "metric_group: classification",
        "output_type: generate",
        "num_fewshot: 0",
        "",
        "prompt_template: |",
        "  Answer the following multiple-choice question by responding with only the letter (A, B, C, or D) of the correct answer. Do not include any explanation.",
        "",
        "  Subject: {{ subject }}",
        "  Question: {{ question }}",
        "",
        "  A) {{ option_a }}",
        "  B) {{ option_b }}",
        "  C) {{ option_c }}",
        "  D) {{ option_d }}",
        "",
        "  Answer:",
        "",
        "filters:",
        "  - type: strip_whitespace",
        "  - type: truncate_at_newline",
        "  - type: remove_assistant_prefix",
        "  - type: strip_whitespace",
        "",
        "samples:",
    ]

    for s in samples:
        lines.append(f"  - id: {s['id']}")
        lines.append(f"    subject: \"{s['subject']}\"")
        lines.append(f"    question: \"{_yaml_escape(s['question'])}\"")
        lines.append(f"    option_a: \"{_yaml_escape(s['option_a'])}\"")
        lines.append(f"    option_b: \"{_yaml_escape(s['option_b'])}\"")
        lines.append(f"    option_c: \"{_yaml_escape(s['option_c'])}\"")
        lines.append(f"    option_d: \"{_yaml_escape(s['option_d'])}\"")
        lines.append(f"    reference: \"{s['reference']}\"")
        lines.append("")

    output_path.write_text("\n".join(lines), encoding="utf-8")
    logger.info("Wrote %s (%d samples)", output_path, len(samples))


# -- ARC-Challenge ----------------------------------------------------------


def _download_arc_challenge(n_samples: int) -> list[dict[str, Any]]:
    """Download ARC-Challenge from HuggingFace and randomly sample."""
    from datasets import load_dataset

    ds = load_dataset(
        "allenai/ai2_arc", "ARC-Challenge", split="test", trust_remote_code=True
    )
    logger.info("ARC-Challenge: %d questions in test set", len(ds))

    rng = random.Random(SEED)
    n = min(n_samples, len(ds))
    indices = rng.sample(range(len(ds)), n)

    samples: list[dict[str, Any]] = []
    skipped = 0

    for idx in sorted(indices):
        row = ds[idx]
        labels = row["choices"]["label"]
        texts = row["choices"]["text"]
        answer_key = row["answerKey"]

        # Normalize numeric labels to letters
        label_map = {}
        for i, label in enumerate(labels):
            if label in MMLU_LABELS:
                label_map[label] = label
            elif label.isdigit():
                mapped = MMLU_LABELS[int(label) - 1] if int(label) <= 4 else None
                if mapped:
                    label_map[label] = mapped
            else:
                label_map[label] = label

        # Normalize answer key
        ref = label_map.get(answer_key, answer_key)
        if ref not in MMLU_LABELS:
            skipped += 1
            continue

        # Format choices text
        choice_lines = []
        for label, text in zip(labels, texts, strict=False):
            norm_label = label_map.get(label, label)
            choice_lines.append(f"{norm_label}) {text}")
        choices_text = "\n".join(choice_lines)

        samples.append(
            {
                "id": f"arc_challenge_{len(samples):03d}",
                "question": row["question"],
                "choices_text": choices_text,
                "reference": ref,
            }
        )

    if skipped:
        logger.info("ARC: skipped %d questions with non-standard labels", skipped)
    logger.info("ARC-Challenge: %d samples", len(samples))
    return samples


def _write_arc_yaml(samples: list[dict[str, Any]], output_path: Path) -> None:
    """Write ARC-Challenge samples as a YAML task file."""
    lines = [
        f"# Auto-generated by prepare_benchmarks.py on {datetime.now(UTC).strftime('%Y-%m-%d')}",
        f"# Source: allenai/ai2_arc ARC-Challenge (HuggingFace), test split, seed={SEED}",
        f"# Total: {len(samples)} questions",
        "",
        "name: arc_challenge",
        "description: >",
        "  Real ARC-Challenge benchmark questions from allenai/ai2_arc (HuggingFace).",
        f"  {len(samples)} randomly sampled from test set.",
        "  Scored via exact_match on generated answer letter.",
        "task_type: classification",
        "metric_group: classification",
        "output_type: generate",
        "num_fewshot: 0",
        "",
        "prompt_template: |",
        "  Answer the following science question by responding with only the letter of the correct answer. Do not include any explanation.",
        "",
        "  Question: {{ question }}",
        "",
        "  {{ choices_text }}",
        "",
        "  Answer:",
        "",
        "filters:",
        "  - type: strip_whitespace",
        "  - type: truncate_at_newline",
        "  - type: remove_assistant_prefix",
        "  - type: strip_whitespace",
        "",
        "samples:",
    ]

    for s in samples:
        lines.append(f"  - id: {s['id']}")
        # Use block scalar for multi-line choices_text
        lines.append(f"    question: \"{_yaml_escape(s['question'])}\"")
        lines.append("    choices_text: |")
        for choice_line in s["choices_text"].split("\n"):
            lines.append(f"      {choice_line}")
        lines.append(f"    reference: \"{s['reference']}\"")
        lines.append("")

    output_path.write_text("\n".join(lines), encoding="utf-8")
    logger.info("Wrote %s (%d samples)", output_path, len(samples))


# -- Utilities --------------------------------------------------------------


def _yaml_escape(text: str) -> str:
    """Escape special characters for YAML double-quoted strings."""
    return (
        text.replace("\\", "\\\\")
        .replace('"', '\\"')
        .replace("\n", "\\n")
        .replace("\t", "\\t")
    )


def _file_hash(path: Path) -> str:
    """Compute SHA256 hash of a file."""
    h = hashlib.sha256()
    h.update(path.read_bytes())
    return h.hexdigest()[:16]


# -- Main -------------------------------------------------------------------


def main() -> int:
    parser = argparse.ArgumentParser(description="Prepare benchmark YAML files")
    parser.add_argument("-v", "--verbose", action="store_true")
    parser.add_argument(
        "--force", action="store_true", help="Re-download even if output files exist"
    )
    parser.add_argument(
        "--mmlu-per-subject",
        type=int,
        default=MMLU_PER_SUBJECT,
        help=f"MMLU questions per subject (default: {MMLU_PER_SUBJECT})",
    )
    parser.add_argument(
        "--arc-samples",
        type=int,
        default=ARC_N_SAMPLES,
        help=f"ARC-Challenge samples (default: {ARC_N_SAMPLES})",
    )
    args = parser.parse_args()

    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
    )

    _TASKS_DIR.mkdir(parents=True, exist_ok=True)

    mmlu_path = _TASKS_DIR / "mmlu_real.yaml"
    arc_path = _TASKS_DIR / "arc_challenge.yaml"

    # MMLU
    if mmlu_path.exists() and not args.force:
        logger.info(
            "MMLU YAML already exists: %s (use --force to re-download)", mmlu_path
        )
    else:
        logger.info("Downloading MMLU (%d per subject)...", args.mmlu_per_subject)
        mmlu_samples = _download_mmlu(args.mmlu_per_subject)
        _write_mmlu_yaml(mmlu_samples, mmlu_path)

    # ARC-Challenge
    if arc_path.exists() and not args.force:
        logger.info(
            "ARC YAML already exists: %s (use --force to re-download)", arc_path
        )
    else:
        logger.info("Downloading ARC-Challenge (%d samples)...", args.arc_samples)
        arc_samples = _download_arc_challenge(args.arc_samples)
        _write_arc_yaml(arc_samples, arc_path)

    # Verify
    print(f"\n{'='*60}")
    print("Benchmark YAML files prepared:")
    for path in [mmlu_path, arc_path]:
        if path.exists():
            # Count samples
            import yaml

            with open(path, encoding="utf-8") as f:
                data = yaml.safe_load(f)
            n = len(data.get("samples", []))
            print(f"  {path.name}: {n} samples  [{_file_hash(path)}]")
        else:
            print(f"  {path.name}: MISSING")
    print(f"{'='*60}")

    return 0


if __name__ == "__main__":
    sys.exit(main())
